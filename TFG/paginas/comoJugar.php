            <!-- Punto 1 -->
            <h4 class="terminos">1. Objetivo del Juego</h4>
            <br><br>
            <h6 class="terminos">- El objetivo del juego "GuessMaster" es adivinar tanto el título de la canción como el nombre del artista o grupo que la interpreta. </h6>
            <h6 class="terminos">- Los participantes deben escuchar un fragmento de la canción y proporcionar la respuesta correcta.</h6>
            <br><br>

            <!-- Punto 1.2 -->
            <h4 class="terminos">2. Preparación</h4>
            <br><br>
            <h6 class="terminos">- El jugador deberá elegir el género de música que desea adivinar o en su defecto pulsar el botón "empezar" para jugar con todos los géneros al mismo tiempo </h6>
            <br><br>

            <h4 class="terminos">3. Desarrollo del Juego</h4>
            <br><br>
            <h6 class="terminos">- El juego reproducirá un fragmento de una canción seleccionada.
            <h6 class="terminos">- Los participantes deberán escuchar atentamente el fragmento y anotar en un papel su respuesta, indicando tanto el título de la canción como el nombre del artista o grupo.</h6>
            <h6 class="terminos">- Habrá un límite de tiempo para que los participantes escriban sus respuestas antes de pasar a la siguiente canción.</h6>
            <h6 class="terminos">- Una vez que se haya agotado el tiempo, se revelará la respuesta correcta.</h6>
            <br><br>

            <!-- Punto 2.2 -->
            <h4 class="terminos">4. Puntuación</h4>
            <br><br>
            <h6 class="terminos">- Otorga 200 puntos si se acierta tanto el artista como la canción.
            <h6 class="terminos">- Si un participante proporciona solo el título de la canción o solo el nombre del artista, otorga 100 por cada respuesta parcialmente correcta.</h6>
            <br><br>

            <!-- Punto 3 -->
            <h4 class="terminos">5. Rondas y ganador</h4>
            <br><br>
            <h6 class="terminos">- El juego puede consistir en varias rondas, cada ronda tendrá una canción y un artista.</h6>
            <h6 class="terminos">- El ganador será aquel participante que acumule la mayor cantidad de puntos al final de todas las rondas.</h6>
